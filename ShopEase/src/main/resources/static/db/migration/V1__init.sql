drop schema if exists shop_ease;
CREATE schema shop_ease;
use shop_ease;
DROP TABLE IF exists `user`;

create table `users`(
`id` BIGINT not null auto_increment primary key,
`full_name` varchar(50) not null ,
`username` varchar(50) not null unique,
`password` char(68) not null,
`email` varchar(50) not null,
`address` varchar(50) default null,
`account_type` varchar(50) not null
);
/*
create table `user_login`(
`username` varchar(50) not null primary key,
`pw` char(68) not null,
`enabled` tinyint not null,
foreign key (`username`) references `user`(`username`)
);
*/

CREATE TABLE `products` (
    `id` BIGINT NOT NULL PRIMARY KEY AUTO_INCREMENT,
    `vendor_id` BIGINT NOT NULL,
    `title` VARCHAR(50) NOT NULL,
    `desc` VARCHAR(50) DEFAULT NULL,
    `price` DOUBLE DEFAULT 0.0,
    `rating` DOUBLE DEFAULT 0.0,
    `rating_count` INT DEFAULT 0,
    `brand` VARCHAR(50) DEFAULT NULL,
    `category` VARCHAR(50) NOT NULL,
    `image_path` VARCHAR(255) DEFAULT NULL,  
    FULLTEXT INDEX `idx_fulltext` (`title`, `desc`, `brand`, `category`),
    INDEX `idx_price` (`price`),
    INDEX `idx_rating` (`rating`),
    INDEX `idx_rating_count` (`rating` DESC, `rating_count` DESC),
    INDEX `idx_category` (`category`),
    FOREIGN KEY (`vendor_id`) REFERENCES `users` (`id`)
);



create table `product_like`(
`product_id` BIGINT not null,
`user_id` BIGINT not null,
primary key (`product_id`,`user_id`),
foreign key (`user_id`) references `users` (`id`),
foreign key (`product_id`) references `products` (`id`)
);

create table `product_review`(
`id` bigint not null auto_increment primary key,
`rating` tinyint default 0 ,
`user_id` BIGINT not null ,
`product_id` BIGINT not null ,
`comment` TEXT default null,
`created_at` TEXT default null,
foreign key (`user_id`) references `users`(`id`),
foreign key (`product_id`) references `products`(`id`)
);



create table `highly_paid_products`(
`product_id` BIGINT not null primary key,
`product_price` double not null,
foreign key (`product_id`) references `products` (`id`)
);

create table `product_category`(
`product_id`  BIGINT not null,
`category_name` varchar(50) not null,
primary key(`product_id`,`category_name`),
foreign key (`product_id`) references `products` (`id`)
);

create table `roles`(
`username` varchar(50) not null primary key,
`role` varchar(68) not null,
foreign key (`username`) references `users` (`username`)
);

CREATE TABLE `wishlist` (
    `id` BIGINT AUTO_INCREMENT PRIMARY KEY,
    `user_id` BIGINT NOT NULL,
    `product_id` BIGINT ,
    `date_added` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (`user_id`) REFERENCES `users` (`id`),
    FOREIGN KEY (`product_id`) REFERENCES `products` (`id`)
);

CREATE TABLE `orders` (
    `id` BIGINT AUTO_INCREMENT PRIMARY KEY,
    `user_id` BIGINT NOT NULL,
    `product_id` BIGINT NOT NULL,
    `quantity` INT NOT NULL DEFAULT 1,
    `total` double NOT NULL,
    `date_purchased` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (`user_id`) REFERENCES `users` (`id`),
    FOREIGN KEY (`product_id`) REFERENCES `products` (`id`)
);


